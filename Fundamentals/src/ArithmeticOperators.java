public class ArithmeticOperators {
    public static void main(String[] args) {
        int sum, sub, multiply, rest;
        double division;

        sum = 1+1; // 2
        sum++; // 3
        ++sum; // 4
        sub = 2-1; // 1
        sub -= 1; // 0
        multiply = 2*2; // 4
        division = 5/2; // 2.5
        rest = 5%2; // 1

        /* O resultado de uma divisão de 2 números inteiros é um número inteiro,
           do contrário o resultado será um ponto flutuante.
         */
    }
}
