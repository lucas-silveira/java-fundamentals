public class RelationalOperators {
    public static void main(String[] args) {
        boolean resp;

        resp = true == true; // true
        resp = true != false; // true
        resp = 2 > 1; // true
        resp = 2 >= 1; // true
        resp = 2 < 1; // false
        resp = 2 <= 1; // false
    }
}
