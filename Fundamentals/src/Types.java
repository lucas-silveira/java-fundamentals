public class Types {
    public static void main(String[] args) {
        // boolean
        boolean check;
        check = true;
        check = false;

        // char
        char letter;
        letter = 'a';

        // byte (8 bits -> -128 to +127)
        byte aByteNumber;
        aByteNumber = 127;

        // short (16 bits -> -32.768 to +32.767)
        short aShortNumber;
        aShortNumber = 32767;

        // int (32 bits)
        int aIntNumber;
        aIntNumber = 1000000000;

        // long (64 bits)
        long aLongNumber;
        aLongNumber = 1000000000;

        // float (32 bits)
        float aFloatNumber;
        aFloatNumber = 1.44f;

        // double (64 bits)
        double aDoubleNumber;
        aDoubleNumber = 1.44;
    }
}
